# operasi aritmatika : X : + -

# operasi penjumlahan +
a = 25
b = 21 
hasil = a + b
print(a, "+", b, "=", hasil)

# operasi pengurangan -
hasil = a - b
print(a, "-", b, "=", hasil)

# operasi perkalian *
hasil = a * b
print(a, "*", b, "=", hasil)

# operasi pembagian /
hasil = a / b
print(a, "/", b, "=", hasil)

# operasi pangkat **
hasil = a ** b
print(a, "**", b, "=", hasil)

# operasi modulus % (sisa bagi)
hasil = a % b
print(a, "mod", b, "=", hasil)

# operasi floor division //
hasil = a // b
print(a, "floor", b, "=", hasil)

# operasi aritmatika yang menggabungkan semua operasi di atas
gabung = 10 - 6 / 4 % 6 // 5 + 2 * 3 ** 5
print(gabung)

